    private void btnShowReceiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowReceiptActionPerformed
        String tablename;
        Payment p1,p2,p3,p4 = new Payment();
        String format = "%1$-40s %2$6d";
        String line;
        int total=0;
        if(ddStudentType.getSelectedItem().equals("Undergraduate")){
            tablename="student";
        }else{
            tablename="poststudent";
        }
        
        pList=dbOps.getCompulsorySubject((Integer.parseInt(txtRegNo.getText())), (Integer.parseInt(txtYear.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())),tablename);
        
        p1=dbOps.getOptionalSubject1((Integer.parseInt(txtRegNo.getText())), (Integer.parseInt(txtYear.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())),tablename);
        if(p1.getFee()!=0){
            pList.add(p1);
        }
        p2=dbOps.getOptionalSubject2((Integer.parseInt(txtRegNo.getText())), (Integer.parseInt(txtYear.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())),tablename);
        if(p2.getFee()!=0){
            pList.add(p2);
        }
        p3=dbOps.getOptionalSubject3((Integer.parseInt(txtRegNo.getText())), (Integer.parseInt(txtYear.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())),tablename);
        if(p3.getFee()!=0){
            pList.add(p3);
        }
        p4=dbOps.getOptionalSubject4((Integer.parseInt(txtRegNo.getText())), (Integer.parseInt(txtYear.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())),tablename);
        if(p4.getFee()!=0){
            pList.add(p4);
        }
        
        txtReceipt.setText("");
        txtReceipt.append("\n =======================================================\n"
                          +"           Course Fees for semester "+ddSemester.getSelectedItem().toString()+"\n"
                          +" =======================================================\n"
                          +"  "+ddStudentType.getSelectedItem().toString()+" Student Registration No: "+txtRegNo.getText()+"\n"
                          +"  Year: "+txtYear.getText()+"\n\n"
                          +" _______________________________________________________\n\n");
        for(int i=0;i<pList.size();i++){
            String s=pList.get(i).getSubject();
            int f=pList.get(i).getFee();
            total=total+f;
            line=String.format(format,s,f);
            txtReceipt.append("  "+line+ "\n");
            //txtReceipt.append(pList.get(i).getSubject()+":  "+pList.get(i).getFee()+"\n"
            //+"____________________________________________________\n");
        }
        txtReceipt.append(" _______________________________________________________\n");
        line=String.format(format, "Total fees",total);
        txtReceipt.append("  "+line+"\n");
        txtReceipt.append(" _______________________________________________________\n");
    }//GEN-LAST:event_btnShowReceiptActionPerformed
