    private void btnGenerateMessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateMessageActionPerformed
        
        String format = "%1$-40s %2$-5s";
        String line;
        
        
        
        sgList=dbOps.getSubjectGrades((Integer.parseInt(txtRegNo1.getText())),ddStudentType1.getSelectedItem().toString() ,(Integer.parseInt(txtYear1.getText())), (Integer.parseInt(ddSemester.getSelectedItem().toString())));
        
        
        txtMessage.setText("");
        txtMessage.append("\nDear Student,\n\n"
                          +"This is your grades for Year "+txtYear1.getText()+" Semester "+ddSemester1.getSelectedItem().toString()+" enrolled subjects.\n"
                          +"\n"+ddStudentType1.getSelectedItem().toString()+" Student Registration No: "+txtRegNo1.getText()+"\n"
                          +"\n");
        for(int i=0;i<sgList.size();i++){
            String s=sgList.get(i).getSubject();
            String f=sgList.get(i).getGrade();
            
            line=String.format(format,s,f);
            txtMessage.append(""+line+ "\n");
            //txtReceipt.append(pList.get(i).getSubject()+":  "+pList.get(i).getFee()+"\n"
            //+"____________________________________________________\n");
        }
        txtMessage.append("\n\nThank you\nRegards,\n\nDilsha Navodi\nRegistrar\nNSBM\nSri Lanka\n");

    }//GEN-LAST:event_btnGenerateMessageActionPerformed
