    void loadAssignment(String subject, String assignmentName) {
        assignList = dbOps.getAssignment(subject, assignmentName);
        AssignmentDetails assignDetails = new AssignmentDetails(assignList);
        tblAssignments.setModel(assignDetails);
    }
    void addAssignmentNames(String subject){
        int i = 0;
        String[] cmb = dbOps.fillAssignmentNamesCombo(subject);
        while (i < cmb.length) {
            ddAssignmentName.addItem(cmb[i]);
            i++;
        } 
        
    }
