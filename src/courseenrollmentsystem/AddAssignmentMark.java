    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        Assignment assign = new Assignment();
        float w,r,rw;
        assign.setRegNo(Integer.parseInt(txtRegNo.getText()));
        assign.setStudentType(ddStudentType.getSelectedItem().toString());
        assign.setYear(Integer.parseInt(txtYear.getText()));
        assign.setSemester(Integer.parseInt(ddSemester.getSelectedItem().toString()));
        assign.setSubject(ddSubject.getSelectedItem().toString());
        assign.setAssignmentName(txtAssignmentName.getText());
        w=Float.parseFloat(txtWeight.getText());
        assign.setWeight(w);
        r=Float.parseFloat(txtResult.getText());
        assign.setResult(r);
        rw=r*(w/100);
        assign.setResultWithWeight(rw);
     
        if(dbOps.addAssignment(assign)){
            JOptionPane.showMessageDialog(this, "Successfully inserted the assignment marks !!");
            ddSubject1.setSelectedItem(ddSubject.getSelectedItem());
            ddAssignmentName.setSelectedItem(txtAssignmentName.getText());
            loadAssignment(ddSubject.getSelectedItem().toString(),txtAssignmentName.getText());
            txtRegNo.setText("");
            txtResult.setText("");
                      
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while inserting !!");
            this.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnLoadSubjectsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadSubjectsActionPerformed
        ddSubject.removeAllItems();
        String tableName;
        String course;
        if(ddStudentType.getSelectedItem().toString().equals("Undergraduate")){
            tableName="student";
        }else{
            tableName="poststudent";
        }
        
        course=dbOps.getCourse(Integer.parseInt(txtRegNo.getText()),tableName);
        addSubjects(course,Integer.parseInt(txtYear.getText().toString()),Integer.parseInt(ddSemester.getSelectedItem().toString()));
    }//GEN-LAST:event_btnLoadSubjectsActionPerformed

