    void addSubjects() {

        int i = 0;
        String[] cmb = dbOps.fillSubjectCombo();
        while (i < cmb.length) {
            ddSubject1.addItem(cmb[i]);
            i++;
        }
    }
    
    void addSubjects(String course,int year,int semester) {

        int i = 0;
        String[] cmb = dbOps.fillSubjectCombo(course,year,semester);
        while (i < cmb.length) {
            ddSubject.addItem(cmb[i]);
            i++;
        }
    }

